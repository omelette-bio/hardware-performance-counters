static mut a: [[f32;2100]; 2100] = [[0.0; 2100];2100];
static mut b: [[f32;2100]; 2100] = [[0.0; 2100];2100];
static mut c: [[f32;2100]; 2100] = [[0.0; 2100];2100];




fn main()
{
    unsafe{
    for i in 0..2100
    {
        for j in 0..2100
        {
            a[i][j] = 1.5;
            b[i][j] = 2.5;
        }
    }

    for i in 0..2100
    {
        for j in 0..2100
        {
            for k in 0..2100
            {
                c[i][j] = c[i][j] + a[i][k] * b[k][j];
            }
        }
    }

    println!("{} {} {}", c[0][0], c[1050][1050], c[2099][2099]);
    }
}
